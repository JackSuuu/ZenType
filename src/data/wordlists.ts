export type WordListKey = 'english' | 'english_1k' | 'code' | 'japanese'

export const wordLists: Record<WordListKey, string[]> = {
  english: english200,
  english_1k: english1k,
  code: codeWords,
  japanese: japaneseRomaji,
}

export function getRandomWords(language: WordListKey, count: number): string[] {
  const list = wordLists[language] || english200
  const result: string[] = []
  for (let i = 0; i < count; i++) {
    result.push(list[Math.floor(Math.random() * list.length)])
  }
  return result
}

export function getRandomQuote() {
  return quotes[Math.floor(Math.random() * quotes.length)]
}
